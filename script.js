// 検索処理
function searchMonster(type) {
  const input = document.getElementById("searchInput").value;
  const resultsDiv = document.getElementById("results");
  resultsDiv.innerHTML = "";

  const data = type === "hiyakashi" ? hiyakashiData : yozumiData;
  const filtered = data.filter(m => m.name.includes(input));

  if (filtered.length === 0) {
    resultsDiv.innerHTML = "<p>該当の人物は見つかりませんでした。</p>";
    return;
  }

  filtered.forEach(m => {
    const card = document.createElement("div");
    card.className = "monster-card";

    if (type === "hiyakashi") {
      // 冷やかし
      card.innerHTML = `
        <img src="${m.image}" alt="${m.name}">
        <div>
          <h3>${m.name} (タイプ: ${m.type})</h3>
          <p>${m.detail}</p>
        </div>
      `;
    } else {
      // 用済み
      card.innerHTML = `
        <img src="${m.image}" alt="${m.name}">
        <div>
          <h3>${m.name} (金額: ${m.price})</h3>
          <p>${m.detail}</p>
        </div>
      `;
    }

    resultsDiv.appendChild(card);
  });
}